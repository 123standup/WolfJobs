from flask import app, render_template, url_for, flash, redirect, request
from forms import RegistrationForm, LoginForm
import bcrypt
from apps import App

app_object = App()
app = app_object.app
mongo = app_object.mongo

posts = [
    {
        'author': 'Corey Schafer',
        'title': 'Blog Post 1',
        'content': 'First post content',
        'date_posted': 'April 20, 2018'
    },
    {
        'author': 'Jane Doe',
        'title': 'Blog Post 2',
        'content': 'Second post content',
        'date_posted': 'April 21, 2018'
    }
]


@app.route("/")
@app.route("/home")
def home():
    return render_template('home.html', posts=posts)


@app.route("/about")
def about():
    return render_template('about.html', title='About')


@app.route("/register", methods=['GET', 'POST'])
def register():
    form = RegistrationForm()
    if form.validate_on_submit():
        if request.method == 'POST':
            username = request.form.get('username')
            email = request.form.get('email')
            password = request.form.get('password')
            id = mongo.db.ath.insert({'name':username,'email':email,'pwd':bcrypt.hashpw(password.encode("utf-8"),bcrypt.gensalt())})
        flash(f'Account created for {form.username.data}!', 'success')
        return redirect(url_for('home'))
    return render_template('register.html', title='Register', form=form)


@app.route("/login", methods=['GET', 'POST'])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        temp = mongo.db.ath.find_one({'email':form.email.data},{'email','pwd'})
        if temp['email'] == form.email.data and bcrypt.checkpw(form.password.data.encode("utf-8"),temp['pwd']):
            flash('You have been logged in!', 'success')
            return redirect(url_for('home'))
        else:
            flash('Login Unsuccessful. Please check username and password', 'danger')
    return render_template('login.html', title='Login', form=form)


if __name__ == '__main__':
    app.run(debug=True)